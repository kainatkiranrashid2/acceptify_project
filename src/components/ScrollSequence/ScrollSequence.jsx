const ScrollSequence = () => {
  const componentRef = useRef(null);
  const sectionsRef = useRef([]);
  const [currentItemIndex, setCurrentItemIndex] = useState(0);
  const [currentFrame, setCurrentFrame] = useState(0);

  useEffect(() => {
    const component = componentRef.current;
    const sections = sectionsRef.current;

    let mm = gsap.matchMedia();

    mm.add("(min-width: 768px)", () => {
      ScrollTrigger.create({
        trigger: component,
        start: "top top",
        end: "bottom bottom",
        pin: ".image-container",
        anticipatePin: 1,
      });

      sections.forEach((section, index) => {
        ScrollTrigger.create({
          trigger: section,
          start: "top 50%",
          end: "bottom 50%",
          onEnter: () => {
            setCurrentItemIndex(index);
            setCurrentFrame(0);
          },
          onEnterBack: () => {
            setCurrentItemIndex(index);
            setCurrentFrame(items[index].imageSources.length - 1);
          },
          onUpdate: (self) => {
            const progress = self.progress;
            const item = items[index];
            const frameIndex = Math.min(
              Math.floor(progress * item.imageSources.length),
              item.imageSources.length - 1
            );
            setCurrentFrame(frameIndex);
          },
        });
      });

      return () => {
        ScrollTrigger.getAll().forEach((t) => t.kill());
      };
    });

    return () => {
      mm.revert();
    };
  }, []);

  return (
    <div ref={componentRef} className="container relative">
      <div className="flex">
        <div className="w-1/2 pl-8 pr-4">
          {items.map((item, index) => (
            <div
              key={index}
              ref={(el) => (sectionsRef.current[index] = el)}
              className="min-h-screen flex flex-col justify-center">
              <h2 className="text-3xl font-bold mb-4">{item.title}</h2>
              <p>{item.description}</p>
            </div>
          ))}
        </div>
        <div className="w-1/2 image-container">
          <div className="sticky top-0 h-screen flex items-center justify-center">
            {items.map((item, itemIndex) => (
              <div
                key={itemIndex}
                className="absolute top-0 left-0 w-full h-full">
                <img
                  src={item.imageSources[currentFrame]}
                  alt={`Sequence ${item.imageSources[currentFrame]}`}
                  className="absolute top-1/2 left-1/2 transform -translate-x-1/2 -translate-y-1/2 w-full aspect-video object-contain transition-opacity duration-300 ease-in-out"
                  style={{
                    opacity: itemIndex === currentItemIndex ? 1 : 0,
                  }}
                />
              </div>
            ))}
          </div>
        </div>
      </div>
    </div>
  );
};

export default ScrollSequence;
